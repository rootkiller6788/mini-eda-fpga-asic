#include <stdio.h>
#include <string.h>
#include <stdlib.h>
#include <stdarg.h>
#include "riscv_verilog.h"

void verilog_config_default(VerilogConfig *cfg) {
    memset(cfg, 0, sizeof(VerilogConfig));
    cfg->standard    = VERILOG_2001;
    cfg->control     = CTRL_HARDWIRED;
    cfg->hazard      = HAZARD_FULL_FWD;
    cfg->addr_width  = 32;
    cfg->data_width  = 32;
    cfg->include_imem = true;
    cfg->include_dmem = true;
    cfg->include_debug = false;
    cfg->include_uart  = false;
    cfg->include_csr   = true;
    cfg->include_fpu   = false;
    cfg->single_cycle  = false;
    cfg->multi_cycle   = false;
    cfg->pipeline      = true;
    cfg->out_of_order  = false;
    strcpy(cfg->module_name, "riscv_core");
    strcpy(cfg->top_module,  "riscv_top");
}

void verilog_writer_init(VerilogWriter *vw, const char *path,
                         const VerilogConfig *cfg) {
    memset(vw, 0, sizeof(VerilogWriter));
    vw->cfg = *cfg;
    vw->fp  = fopen(path, "w");
    if (!vw->fp) {
        fprintf(stderr, "Error: cannot open %s for writing\n", path);
        return;
    }
    vw->indent_level = 0;
    vw->line_count   = 0;
}

void verilog_writer_close(VerilogWriter *vw) {
    if (vw->fp) {
        fclose(vw->fp);
        vw->fp = NULL;
    }
}

static void emit_indent(VerilogWriter *vw) {
    for (uint32_t i = 0; i < vw->indent_level; i++)
        fputs("    ", vw->fp);
}

void verilog_comment(VerilogWriter *vw, const char *fmt, ...) {
    if (!vw->fp) return;
    emit_indent(vw);
    fputs("// ", vw->fp);
    va_list args;
    va_start(args, fmt);
    vfprintf(vw->fp, fmt, args);
    va_end(args);
    fputc('\n', vw->fp);
    vw->line_count++;
}

void verilog_stmt(VerilogWriter *vw, const char *fmt, ...) {
    if (!vw->fp) return;
    emit_indent(vw);
    va_list args;
    va_start(args, fmt);
    vfprintf(vw->fp, fmt, args);
    va_end(args);
    if (fmt[strlen(fmt) - 1] != '\n') fputc('\n', vw->fp);
    vw->line_count++;
}

void verilog_indent(VerilogWriter *vw) {
    vw->indent_level++;
}

void verilog_dedent(VerilogWriter *vw) {
    if (vw->indent_level > 0) vw->indent_level--;
}

void verilog_emit_header(VerilogWriter *vw) {
    if (!vw->fp) return;
    fprintf(vw->fp, "// ============================================\n");
    fprintf(vw->fp, "//  RISC-V RV32I Processor - Verilog RTL\n");
    fprintf(vw->fp, "//  Generated by mini-riscv-arch\n");
    fprintf(vw->fp, "//  Standard: Verilog-2001\n");
    fprintf(vw->fp, "// ============================================\n\n");
    vw->line_count += 5;
}

void verilog_emit_footer(VerilogWriter *vw) {
    if (!vw->fp) return;
    fprintf(vw->fp, "\nendmodule\n");
    vw->line_count += 2;
}

void verilog_emit_module_decl(VerilogWriter *vw, const char *name,
                              const char **ports, uint32_t port_count) {
    if (!vw->fp) return;
    fprintf(vw->fp, "module %s ", name);
    if (port_count > 0) {
        fprintf(vw->fp, "(\n");
        for (uint32_t i = 0; i < port_count; i++) {
            fprintf(vw->fp, "    %s", ports[i]);
            if (i < port_count - 1) fputs(",\n", vw->fp);
            else fputs("\n", vw->fp);
        }
        fprintf(vw->fp, ");\n");
    } else {
        fprintf(vw->fp, "();\n");
    }
    vw->line_count += 3 + port_count;
}

void verilog_emit_ports(VerilogWriter *vw, const char **inputs,
                        uint32_t in_count, const char **outputs,
                        uint32_t out_count) {
    if (!vw->fp) return;

    for (uint32_t i = 0; i < in_count; i++) {
        fprintf(vw->fp, "    input  wire %s;\n", inputs[i]);
    }
    for (uint32_t i = 0; i < out_count; i++) {
        fprintf(vw->fp, "    output wire %s;\n", outputs[i]);
    }
    vw->line_count += in_count + out_count;
}

void verilog_emit_wire(VerilogWriter *vw, const char *name, uint32_t width) {
    if (!vw->fp) return;
    emit_indent(vw);
    if (width > 1)
        fprintf(vw->fp, "wire [%u:0] %s;\n", width - 1, name);
    else
        fprintf(vw->fp, "wire %s;\n", name);
    vw->line_count++;
}

void verilog_emit_reg(VerilogWriter *vw, const char *name, uint32_t width) {
    if (!vw->fp) return;
    emit_indent(vw);
    if (width > 1)
        fprintf(vw->fp, "reg [%u:0] %s;\n", width - 1, name);
    else
        fprintf(vw->fp, "reg %s;\n", name);
    vw->line_count++;
}

void verilog_emit_assign(VerilogWriter *vw, const char *lhs, const char *rhs) {
    if (!vw->fp) return;
    emit_indent(vw);
    fprintf(vw->fp, "assign %s = %s;\n", lhs, rhs);
    vw->line_count++;
}

void verilog_emit_always_comb_begin(VerilogWriter *vw) {
    if (!vw->fp) return;
    emit_indent(vw);
    fprintf(vw->fp, "always @(*) begin\n");
    vw->line_count++;
    vw->indent_level++;
}

void verilog_emit_always_ff_begin(VerilogWriter *vw, const char *edge,
                                  const char *clock) {
    if (!vw->fp) return;
    emit_indent(vw);
    fprintf(vw->fp, "always @(%s %s) begin\n", edge, clock);
    vw->line_count++;
    vw->indent_level++;
}

void verilog_emit_always_end(VerilogWriter *vw) {
    if (!vw->fp) return;
    vw->indent_level--;
    emit_indent(vw);
    fprintf(vw->fp, "end\n");
    vw->line_count++;
}

void verilog_emit_if_begin(VerilogWriter *vw, const char *cond) {
    if (!vw->fp) return;
    emit_indent(vw);
    fprintf(vw->fp, "if (%s) begin\n", cond);
    vw->line_count++;
    vw->indent_level++;
}

void verilog_emit_if_end(VerilogWriter *vw) {
    if (!vw->fp) return;
    vw->indent_level--;
    emit_indent(vw);
    fprintf(vw->fp, "end\n");
    vw->line_count++;
}

void verilog_emit_case_begin(VerilogWriter *vw, const char *sel) {
    if (!vw->fp) return;
    emit_indent(vw);
    fprintf(vw->fp, "case (%s)\n", sel);
    vw->line_count++;
    vw->indent_level++;
}

void verilog_emit_case_item(VerilogWriter *vw, const char *value) {
    if (!vw->fp) return;
    emit_indent(vw);
    fprintf(vw->fp, "%s: begin\n", value);
    vw->line_count++;
    vw->indent_level++;
}

void verilog_emit_case_end(VerilogWriter *vw) {
    if (!vw->fp) return;
    vw->indent_level--;
    emit_indent(vw);
    fprintf(vw->fp, "endcase\n");
    vw->line_count++;
}

void verilog_emit_pc_logic(VerilogWriter *vw) {
    if (!vw->fp) return;
    verilog_comment(vw, "PC Logic");
    verilog_emit_reg(vw, "pc", 32);
    verilog_emit_reg(vw, "pc_next", 32);
    verilog_stmt(vw, "");
    verilog_emit_always_ff_begin(vw, "posedge", "clk");
    verilog_emit_if_begin(vw, "!rst_n");
    verilog_stmt(vw, "pc <= 32'h00000000;");
    verilog_emit_if_end(vw);
    verilog_stmt(vw, "else begin");
    vw->indent_level++;
    verilog_stmt(vw, "pc <= pc_next;");
    vw->indent_level--;
    verilog_stmt(vw, "end");
    verilog_emit_always_end(vw);
}

void verilog_emit_imem(VerilogWriter *vw, uint32_t depth) {
    if (!vw->fp) return;
    verilog_comment(vw, "Instruction Memory");
    verilog_stmt(vw, "reg [31:0] imem [0:%u];", depth - 1);
    verilog_stmt(vw, "wire [31:0] inst;");
    verilog_stmt(vw, "");
    verilog_emit_assign(vw, "inst", "imem[pc[%u:2]]", (uint32_t)31);
}

void verilog_emit_register_file(VerilogWriter *vw) {
    if (!vw->fp) return;
    verilog_comment(vw, "Register File (x0 hardwired to 0)");
    verilog_stmt(vw, "reg [31:0] rf [31:0];");
    verilog_stmt(vw, "wire [31:0] rs1_data, rs2_data;");
    verilog_stmt(vw, "wire [31:0] wb_data;");
    verilog_stmt(vw, "");
    verilog_emit_assign(vw, "rs1_data", "(rs1_addr == 5'd0) ? 32'd0 : rf[rs1_addr]");
    verilog_emit_assign(vw, "rs2_data", "(rs2_addr == 5'd0) ? 32'd0 : rf[rs2_addr]");
    verilog_stmt(vw, "");
    verilog_emit_always_ff_begin(vw, "posedge", "clk");
    verilog_emit_if_begin(vw, "wb_en && (rd_addr != 5'd0)");
    verilog_stmt(vw, "rf[rd_addr] <= wb_data;");
    verilog_emit_if_end(vw);
    verilog_emit_always_end(vw);
}

void verilog_emit_alu(VerilogWriter *vw) {
    if (!vw->fp) return;
    verilog_comment(vw, "ALU");
    verilog_stmt(vw, "reg [31:0] alu_result;");
    verilog_stmt(vw, "wire [31:0] alu_a, alu_b;");
    verilog_stmt(vw, "wire [3:0] alu_op;");
    verilog_stmt(vw, "");
    verilog_emit_always_comb_begin(vw);
    verilog_emit_case_begin(vw, "alu_op");
    verilog_emit_case_item(vw, "4'd0");
    verilog_stmt(vw, "alu_result = alu_a + alu_b;");
    verilog_emit_case_end(vw);
    verilog_emit_case_item(vw, "4'd1");
    verilog_stmt(vw, "alu_result = alu_a - alu_b;");
    verilog_emit_case_end(vw);
    verilog_emit_case_item(vw, "4'd2");
    verilog_stmt(vw, "alu_result = alu_a << alu_b[4:0];");
    verilog_emit_case_end(vw);
    verilog_emit_case_item(vw, "4'd3");
    verilog_stmt(vw, "alu_result = ($signed(alu_a) < $signed(alu_b)) ? 32'd1 : 32'd0;");
    verilog_emit_case_end(vw);
    verilog_emit_case_item(vw, "4'd4");
    verilog_stmt(vw, "alu_result = (alu_a < alu_b) ? 32'd1 : 32'd0;");
    verilog_emit_case_end(vw);
    verilog_emit_case_item(vw, "4'd5");
    verilog_stmt(vw, "alu_result = alu_a ^ alu_b;");
    verilog_emit_case_end(vw);
    verilog_emit_case_item(vw, "4'd6");
    verilog_stmt(vw, "alu_result = alu_a >> alu_b[4:0];");
    verilog_emit_case_end(vw);
    verilog_emit_case_item(vw, "4'd7");
    verilog_stmt(vw, "alu_result = $signed(alu_a) >>> alu_b[4:0];");
    verilog_emit_case_end(vw);
    verilog_emit_case_item(vw, "4'd8");
    verilog_stmt(vw, "alu_result = alu_a | alu_b;");
    verilog_emit_case_end(vw);
    verilog_emit_case_item(vw, "4'd9");
    verilog_stmt(vw, "alu_result = alu_a & alu_b;");
    verilog_emit_case_end(vw);
    verilog_emit_case_item(vw, "default");
    verilog_stmt(vw, "alu_result = 32'd0;");
    verilog_emit_case_end(vw);
    verilog_emit_case_end(vw);
    verilog_emit_always_end(vw);
}

