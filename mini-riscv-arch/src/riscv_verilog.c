void verilog_emit_control_fsm(VerilogWriter *vw) {
    if (!vw->fp) return;
    verilog_comment(vw, "Control FSM States");
    verilog_stmt(vw, "localparam S_IF   = 3'd0;");
    verilog_stmt(vw, "localparam S_ID   = 3'd1;");
    verilog_stmt(vw, "localparam S_EX   = 3'd2;");
    verilog_stmt(vw, "localparam S_MEM  = 3'd3;");
    verilog_stmt(vw, "localparam S_WB   = 3'd4;");
    verilog_stmt(vw, "");
    verilog_emit_reg(vw, "state", 3);
    verilog_emit_reg(vw, "next_state", 3);
    verilog_stmt(vw, "");
    verilog_emit_always_ff_begin(vw, "posedge", "clk");
    verilog_emit_if_begin(vw, "!rst_n");
    verilog_stmt(vw, "state <= S_IF;");
    verilog_emit_if_end(vw);
    verilog_stmt(vw, "else begin");
    vw->indent_level++;
    verilog_stmt(vw, "state <= next_state;");
    vw->indent_level--;
    verilog_stmt(vw, "end");
    verilog_emit_always_end(vw);
    verilog_stmt(vw, "");
    verilog_emit_always_comb_begin(vw);
    verilog_emit_case_begin(vw, "state");
    verilog_emit_case_item(vw, "S_IF");
    verilog_stmt(vw, "next_state = stall ? S_IF : S_ID;");
    verilog_emit_case_end(vw);
    verilog_emit_case_item(vw, "S_ID");
    verilog_stmt(vw, "next_state = stall ? S_ID : S_EX;");
    verilog_emit_case_end(vw);
    verilog_emit_case_item(vw, "S_EX");
    verilog_stmt(vw, "next_state = S_MEM;");
    verilog_emit_case_end(vw);
    verilog_emit_case_item(vw, "S_MEM");
    verilog_stmt(vw, "next_state = S_WB;");
    verilog_emit_case_end(vw);
    verilog_emit_case_item(vw, "S_WB");
    verilog_stmt(vw, "next_state = S_IF;");
    verilog_emit_case_end(vw);
    verilog_emit_case_item(vw, "default");
    verilog_stmt(vw, "next_state = S_IF;");
    verilog_emit_case_end(vw);
    verilog_emit_case_end(vw);
    verilog_emit_always_end(vw);
}

