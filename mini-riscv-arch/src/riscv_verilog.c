void verilog_emit_top_module(VerilogWriter *vw, const VerilogConfig *cfg) {
    if (!vw->fp) return;

    const char *ports[] = {
        "input  wire        clk",
        "input  wire        rst_n",
        "output wire [31:0] pc_out",
        "output wire [31:0] inst_out"
    };
    verilog_emit_module_decl(vw, cfg->top_module, ports, 4);
    verilog_stmt(vw, "");

    verilog_emit_wire(vw, "clk", 1);
    verilog_emit_wire(vw, "rst_n", 1);
    verilog_emit_wire(vw, "pc_out", 32);
    verilog_emit_wire(vw, "inst_out", 32);
    verilog_stmt(vw, "");

    verilog_comment(vw, "Instantiate the RISC-V core");
    verilog_stmt(vw, "%s u_core (", cfg->module_name);
    verilog_stmt(vw, "    .clk      (clk),");
    verilog_stmt(vw, "    .rst_n    (rst_n),");
    verilog_stmt(vw, "    .pc       (pc_out),");
    verilog_stmt(vw, "    .inst     (inst_out)");
    verilog_stmt(vw, ");");
    verilog_emit_footer(vw);
}

void verilog_generate_processor(const char *path, const VerilogConfig *cfg) {
    if (!path || !cfg) return;

    VerilogWriter vw;
    verilog_writer_init(&vw, path, cfg);
    if (!vw.fp) return;

    verilog_emit_header(&vw);
    verilog_emit_top_module(&vw, cfg);
    verilog_writer_close(&vw);

    printf("Generated Verilog processor: %s (%u lines)\n",
           path, vw.line_count);
}

void verilog_generate_testbench(const char *path, const VerilogConfig *cfg) {
    if (!path || !cfg) return;

    FILE *fp = fopen(path, "w");
    if (!fp) {
        fprintf(stderr, "Error: cannot open %s for writing\n", path);
        return;
    }

    fprintf(fp, "// ============================================\n");
    fprintf(fp, "//  RISC-V Processor Testbench\n");
    fprintf(fp, "//  Generated by mini-riscv-arch\n");
    fprintf(fp, "// ============================================\n\n");

    fprintf(fp, "`timescale 1ns / 1ps\n\n");
    fprintf(fp, "module tb_%s;\n\n", cfg->top_module);
    fprintf(fp, "    reg        clk;\n");
    fprintf(fp, "    reg        rst_n;\n");
    fprintf(fp, "    wire [31:0] pc;\n");
    fprintf(fp, "    wire [31:0] inst;\n\n");

    fprintf(fp, "    %s uut (\n", cfg->top_module);
    fprintf(fp, "        .clk   (clk),\n");
    fprintf(fp, "        .rst_n (rst_n),\n");
    fprintf(fp, "        .pc    (pc),\n");
    fprintf(fp, "        .inst  (inst)\n");
    fprintf(fp, "    );\n\n");

    fprintf(fp, "    always #5 clk = ~clk;\n\n");

    fprintf(fp, "    initial begin\n");
    fprintf(fp, "        $dumpfile(\"tb_%s.vcd\");\n", cfg->top_module);
    fprintf(fp, "        $dumpvars(0, tb_%s);\n\n", cfg->top_module);
    fprintf(fp, "        clk   = 0;\n");
    fprintf(fp, "        rst_n = 0;\n");
    fprintf(fp, "        #20 rst_n = 1;\n\n");

    fprintf(fp, "        #200;\n");
    fprintf(fp, "        $display(\"Testbench complete. PC = %%h\", pc);\n");
    fprintf(fp, "        $finish;\n");
    fprintf(fp, "    end\n\n");

    fprintf(fp, "endmodule\n");

    fclose(fp);
    printf("Generated Verilog testbench: %s\n", path);
}
