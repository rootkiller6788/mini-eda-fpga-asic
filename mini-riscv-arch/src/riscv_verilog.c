void verilog_emit_hazard_unit(VerilogWriter *vw) {
    if (!vw->fp) return;
    verilog_comment(vw, "Hazard Detection Unit");
    verilog_stmt(vw, "reg stall;");
    verilog_stmt(vw, "reg flush;");
    verilog_stmt(vw, "reg [1:0] forward_a;");
    verilog_stmt(vw, "reg [1:0] forward_b;");
    verilog_stmt(vw, "");
    verilog_emit_always_comb_begin(vw);
    verilog_stmt(vw, "stall     = 1'b0;");
    verilog_stmt(vw, "flush     = 1'b0;");
    verilog_stmt(vw, "forward_a = 2'b00;");
    verilog_stmt(vw, "forward_b = 2'b00;");

    verilog_comment(vw, "Load-use hazard");
    verilog_emit_if_begin(vw, "ex_mem_reg_write && ex_mem_rd != 5'd0 && "
                              "id_ex_rs1 == ex_mem_rd");
    verilog_stmt(vw, "stall = 1'b1;");
    verilog_emit_if_end(vw);

    verilog_comment(vw, "Forwarding EX/MEM -> EX");
    verilog_emit_if_begin(vw, "ex_mem_reg_write && ex_mem_rd != 5'd0 && "
                              "ex_mem_rd == id_ex_rs1");
    verilog_stmt(vw, "forward_a = 2'b10;");
    verilog_emit_if_end(vw);
    verilog_emit_if_begin(vw, "ex_mem_reg_write && ex_mem_rd != 5'd0 && "
                              "ex_mem_rd == id_ex_rs2");
    verilog_stmt(vw, "forward_b = 2'b10;");
    verilog_emit_if_end(vw);

    verilog_comment(vw, "Forwarding MEM/WB -> EX");
    verilog_emit_if_begin(vw, "mem_wb_reg_write && mem_wb_rd != 5'd0 && "
                              "!(ex_mem_reg_write && ex_mem_rd != 5'd0 && "
                              "ex_mem_rd == id_ex_rs1) && mem_wb_rd == id_ex_rs1");
    verilog_stmt(vw, "forward_a = 2'b01;");
    verilog_emit_if_end(vw);
    verilog_emit_if_begin(vw, "mem_wb_reg_write && mem_wb_rd != 5'd0 && "
                              "!(ex_mem_reg_write && ex_mem_rd != 5'd0 && "
                              "ex_mem_rd == id_ex_rs2) && mem_wb_rd == id_ex_rs2");
    verilog_stmt(vw, "forward_b = 2'b01;");
    verilog_emit_if_end(vw);
    verilog_emit_always_end(vw);
}

void verilog_emit_forwarding_logic(VerilogWriter *vw) {
    if (!vw->fp) return;
    verilog_comment(vw, "Forwarding Multiplexers");
    verilog_stmt(vw, "reg [31:0] operand_a;");
    verilog_stmt(vw, "reg [31:0] operand_b;");
    verilog_stmt(vw, "");
    verilog_emit_always_comb_begin(vw);
    verilog_emit_case_begin(vw, "forward_a");
    verilog_emit_case_item(vw, "2'b00");
    verilog_stmt(vw, "operand_a = rs1_data;");
    verilog_emit_case_end(vw);
    verilog_emit_case_item(vw, "2'b01");
    verilog_stmt(vw, "operand_a = mem_wb_result;");
    verilog_emit_case_end(vw);
    verilog_emit_case_item(vw, "2'b10");
    verilog_stmt(vw, "operand_a = ex_mem_result;");
    verilog_emit_case_end(vw);
    verilog_emit_case_item(vw, "default");
    verilog_stmt(vw, "operand_a = rs1_data;");
    verilog_emit_case_end(vw);
    verilog_emit_case_end(vw);
    verilog_stmt(vw, "");
    verilog_emit_case_begin(vw, "forward_b");
    verilog_emit_case_item(vw, "2'b00");
    verilog_stmt(vw, "operand_b = rs2_data;");
    verilog_emit_case_end(vw);
    verilog_emit_case_item(vw, "2'b01");
    verilog_stmt(vw, "operand_b = mem_wb_result;");
    verilog_emit_case_end(vw);
    verilog_emit_case_item(vw, "2'b10");
    verilog_stmt(vw, "operand_b = ex_mem_result;");
    verilog_emit_case_end(vw);
    verilog_emit_case_item(vw, "default");
    verilog_stmt(vw, "operand_b = rs2_data;");
    verilog_emit_case_end(vw);
    verilog_emit_case_end(vw);
    verilog_emit_always_end(vw);
}

void verilog_emit_stall_logic(VerilogWriter *vw) {
    if (!vw->fp) return;
    verilog_comment(vw, "Stall Logic - Freeze PC and Flush ID/EX");
    verilog_stmt(vw, "wire pc_write;");
    verilog_stmt(vw, "wire if_id_write;");
    verilog_stmt(vw, "");
    verilog_emit_assign(vw, "pc_write", "~stall");
    verilog_emit_assign(vw, "if_id_write", "~stall");
    verilog_stmt(vw, "");
    verilog_emit_always_ff_begin(vw, "posedge", "clk");
    verilog_emit_if_begin(vw, "~stall");
    verilog_stmt(vw, "pc <= pc_next;");
    verilog_emit_if_end(vw);
    verilog_emit_always_end(vw);
}

void verilog_emit_csr_block(VerilogWriter *vw) {
    if (!vw->fp) return;
    verilog_comment(vw, "CSR Registers (M-mode)");
    verilog_stmt(vw, "reg [31:0] mstatus;");
    verilog_stmt(vw, "reg [31:0] mepc;");
    verilog_stmt(vw, "reg [31:0] mtvec;");
    verilog_stmt(vw, "reg [31:0] mcause;");
    verilog_stmt(vw, "reg [31:0] mie;");
    verilog_stmt(vw, "reg [31:0] mip;");
    verilog_stmt(vw, "reg [63:0] mcycle;");
    verilog_stmt(vw, "");
    verilog_emit_always_ff_begin(vw, "posedge", "clk");
    verilog_emit_if_begin(vw, "!rst_n");
    verilog_stmt(vw, "mstatus <= 32'h00001800;");
    verilog_stmt(vw, "mepc    <= 32'd0;");
    verilog_stmt(vw, "mtvec   <= 32'd0;");
    verilog_stmt(vw, "mcause  <= 32'd0;");
    verilog_stmt(vw, "mcycle  <= 64'd0;");
    verilog_emit_if_end(vw);
    verilog_stmt(vw, "else begin");
    vw->indent_level++;
    verilog_stmt(vw, "mcycle <= mcycle + 64'd1;");
    vw->indent_level--;
    verilog_stmt(vw, "end");
    verilog_emit_always_end(vw);
}

void verilog_emit_timer(VerilogWriter *vw) {
    if (!vw->fp) return;
    verilog_comment(vw, "Timer (mtime / mtimecmp)");
    verilog_stmt(vw, "reg [63:0] mtime;");
    verilog_stmt(vw, "reg [63:0] mtimecmp;");
    verilog_stmt(vw, "wire timer_interrupt;");
    verilog_stmt(vw, "");
    verilog_emit_assign(vw, "timer_interrupt", "mtime >= mtimecmp");
    verilog_stmt(vw, "");
    verilog_emit_always_ff_begin(vw, "posedge", "clk");
    verilog_emit_if_begin(vw, "!rst_n");
    verilog_stmt(vw, "mtime    <= 64'd0;");
    verilog_stmt(vw, "mtimecmp <= 64'hFFFFFFFFFFFFFFFF;");
    verilog_emit_if_end(vw);
    verilog_stmt(vw, "else begin");
    vw->indent_level++;
    verilog_stmt(vw, "mtime <= mtime + 64'd1;");
    vw->indent_level--;
    verilog_stmt(vw, "end");
    verilog_emit_always_end(vw);
}

