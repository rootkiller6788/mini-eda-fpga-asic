/**
 * demo01_noc_sim.c ? L7 Application: Full NoC Simulator Demo
 *
 * Cycle-accurate NoC simulation with topology construction,
 * routing, flow control, performance metrics collection,
 * and visualization.
 */

#include <stdio.h>
#include <stdlib.h>
#include "noc_topology.h"
#include "noc_router.h"
#include "noc_routing.h"
#include "noc_flowctrl.h"
#include "noc_perf.h"
#include "noc_deadlock.h"

int main(void) {
    printf("????????????????????????????????????????????\n");
    printf("?  Demo 01: Full NoC Simulation           ?\n");
    printf("????????????????????????????????????????????\n\n");

    int32_t k = 4;
    noc_topology_t *mesh = noc_topo_create_mesh_2d(k);
    if (!mesh) { printf("ERROR: topology creation failed\n"); return 1; }

    printf("Topology: %s\n", mesh->name);
    printf("Nodes: %d, Edges: %d\n\n", mesh->num_nodes, mesh->num_edges);

    /* ??? Configure simulation ??????????????????????????????????? */
    noc_simulator_t sim;
    noc_simulator_init(&sim, mesh, TRAFFIC_UNIFORM, 4, 0.05,
                       ROUTE_XY, 12345);

    printf("??? Running Simulation ???\n\n");

    /* Run for 200 cycles, print progress every 50 */
    int32_t batch;
    for (batch = 0; batch < 4; batch++) {
        noc_simulator_run(&sim, 50);

        noc_perf_metrics_t m = noc_simulator_collect_metrics(&sim);
        printf("  Cycle %3llu: Offered=%.4f, Accepted=%.4f, "
               "Lat=%.1f, BufOcc=%.3f\n",
               (unsigned long long)sim.cycle,
               m.offered_throughput, m.accepted_throughput,
               m.avg_packet_latency, m.avg_buffer_occupancy);
    }

    /* ??? Final metrics ?????????????????????????????????????????? */
    printf("\n??? Final Performance Metrics ???\n\n");

    noc_perf_metrics_t final_m = noc_simulator_collect_metrics(&sim);
    noc_perf_print(&final_m);

    /* ??? Deadlock check ????????????????????????????????????????? */
    printf("\n??? Deadlock Check ???\n\n");

    noc_channel_dep_graph_t cdg = noc_cdg_build(ROUTE_XY, k);
    int has_cycle = noc_cdg_has_cycle(&cdg);
    printf("  XY Routing deadlock-free: %s\n", has_cycle ? "NO ?" : "YES ?");

    /* ??? Livelock check on sample path ????????????????????????? */
    noc_path_t path = noc_topo_find_path_xy(mesh, 0, 15);
    int ll_free = noc_path_livelock_free(&path, mesh);
    printf("  Path (0?15) livelock-free: %s\n", ll_free ? "YES ?" : "NO ?");

    noc_simulator_destroy(&sim);
    noc_topo_free(mesh);

    printf("\nSimulation complete.\n");
    return 0;
}
